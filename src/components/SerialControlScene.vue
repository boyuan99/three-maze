              </div>
            </div>
            <div class="scene-info">
              <h2 class="scene-title">Load Custom Scene</h2>
              <p class="scene-description">
                Import a custom JSON scene configuration
              </p>
            </div>
            <input
              ref="fileInput"
              type="file"
              accept=".json"
              class="hidden-input"
              @change="handleFileSelect"
            >
          </div>
          
          <div 
            v-for="scene in serialControlScenes.filter(s => !s.id.startsWith('serial_custom_'))" 
            :key="scene.id"
            class="scene-card"
          >
            <div class="scene-preview">
              <div v-if="!previewsLoaded || !previews[scene.id]" class="preview-loading">
                Loading preview...
              </div>
              <img
                v-else
                :src="previews[scene.id]"
                :alt="scene.name"
                class="preview-image"
              />
            </div>
            <div class="scene-info">
              <div class="scene-header">
                <h2 class="scene-title">{{ scene.name }}</h2>
              </div>
              <p class="scene-description">{{ scene.description }}</p>
              
              <div class="scene-actions">
                <button class="action-button open-button" @click="handleOpenScene(scene.id)">
                  Open Scene
                </button>
              </div>
            </div>
          </div>
          
          <div 
            v-for="scene in serialControlScenes.filter(s => s.id.startsWith('serial_custom_'))" 
            :key="scene.id"
            class="scene-card"
          >
            <div class="scene-preview">
              <div v-if="!previewsLoaded || !previews[scene.id]" class="preview-loading">
                Loading preview...
              </div>
              <img
                v-else
                :src="previews[scene.id]"
                :alt="scene.name"
                class="preview-image"
              />
            </div>
            <div class="scene-info">
              <div class="scene-header">
                <h2 class="scene-title">{{ scene.name }}</h2>
                <button
                  class="delete-button"
                  @click.stop="handleDeleteScene(scene.id, $event)"
                  title="Delete custom scene"
                >
                  ×
                </button>
              </div>
              <p class="scene-description">{{ scene.description }}</p>
              
              <div v-if="sceneControlFiles[scene.id]" class="control-file-info">
                <p>✓ Control File: {{ sceneControlFiles[scene.id].name }}</p>
              </div>
              <div v-else class="control-file-info">
                <p style="color: #ff6b6b;">⚠ No control file selected</p>
              </div>
              
              <div class="scene-actions">
                <button 
                  class="action-button open-button" 
                  @click="handleOpenScene(scene.id)"
                  :disabled="!canOpenScene(scene.id)"
                  :class="{ 'disabled': !canOpenScene(scene.id) }"
                >
                  Open Scene
                </button>
                <button class="action-button code-button" @click="selectControlCodeFile(scene.id)">
                  Select Control File
                </button>
              </div>
            </div>
          </div>
        </div>
        
        <input
          ref="codeFileInput"
          type="file"
          accept=".js,.py,.json"
          class="hidden-input"
          @change="handleCodeFileSelect"
        >
        
        <div v-if="loadingScene" class="loading-overlay">
          <div class="loading-content">
            <div class="loading-spinner"></div>
            <p>Loading scene...</p>
          </div>
        </div>
      </div>
    </div>
  </div>
</template>

